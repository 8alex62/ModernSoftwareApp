namespace ModernSoftwareApp
{
    /// <summary>
    /// Provides basic mathematical operations.
    /// </summary>
    /// <remarks>The <see cref="Calculator"/> class offers methods for performing simple arithmetic
    /// calculations. It is designed for straightforward use cases where basic operations, such as addition, are
    /// required.</remarks>
    public class Calculator
    {
        /// <summary>
        /// Adds two integers and returns the result.
        /// </summary>
        /// <param name="a">The first integer to add.</param>
        /// <param name="b">The second integer to add.</param>
        /// <returns>The sum of <paramref name="a"/> and <paramref name="b"/>.</returns>
        public int Add(int a, int b) => a + b;
    }
}
